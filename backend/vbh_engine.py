def make_signal(
    symbol_display: str,
    api_symbol: str,
    current_hour_ohlc: dict,
    last_price: float,
    stats_agg: dict,
    stats_con: dict,
    dollar_risk: float = 50.0,
    hour_override: int | None = None,
    daily_bias: str | None = None,   # 'LONG' | 'SHORT' — RTH open vs prev_settle
    et_minute: int | None = None,    # minutes since midnight ET (kept for compatibility)
    stats_wide: dict | None = None,  # WIDE (extra-conservative) model levels
    cr: dict | None = None,          # Clearing Range levels {high, low, mid, entry_long, entry_short, complete}
    cr_breached: str | None = None,  # 'LONG' | 'SHORT' — which CR extreme was first hit
) -> list[dict] | None:
    """
    Build signal rows for one symbol using HBMR rules.

    HBMR level ordering (low → high price):
      hourlyRLH (h_high−L1)  ← LONG entry
      shortT    (h_high−L4)  ← NEAR/NEUTRAL boundary (LONG side)
          [NEUTRAL ZONE]
      longT     (h_low+L4)   ← NEAR/NEUTRAL boundary (SHORT side)
      hourlyRHL (h_low+L1)   ← SHORT entry

    Phase 1 (bias active): bias-driven, strict side suppression.  Bias holds until IB
    is breached in the opposite direction — NO fixed time cutoff.
    Phase 2 (no bias): position-driven, both sides shown.

    CR fade trade: after IB breach, NEAR until price retreats to CR mid → ENTRY.
    """
    now_et = datetime.now(ET)
    h = hour_override if hour_override is not None else now_et.hour

    agg = stats_agg.get(h, (0, 0, 0, 0))
    con = stats_con.get(h, (0, 0, 0, 0))

    if agg[2] == 0 and con[2] == 0:
        return None   # no data for this hour

    h_open  = current_hour_ohlc.get('open',  last_price)
    h_high  = current_hour_ohlc.get('high',  last_price)
    h_low   = current_hour_ohlc.get('low',   last_price)
    current_range = h_high - h_low

    wide = stats_wide.get(h, (0, 0, 0, 0)) if stats_wide else (0, 0, 0, 0)

    results = []
    models = [('AGG', agg), ('CON', con)]
    if wide[2] > 0:
        models.append(('WIDE', wide))
    for label, (l1, l2, l3, l4) in models:
        if l3 == 0:
            continue
        # VBH stocks only provide L1/L2/L3; derive L4 from the L1–L2 span
        # (same 0.385·σ relationship measured from 2022 futures data).
        if l4 == 0 and l1 > 0 and l2 > l1:
            l4 = max(l1 - 0.385 * (l2 - l1), l1 * 0.10)

        # ── Box levels (exact ThinkScript variable names) ──────────────────
        hourlyRHL = h_low  + l1   # Lower cyan  — SHORT entry trigger
        hourlyRLH = h_high - l1   # Upper cyan  — LONG  entry trigger
        hourlyRHA = h_low  + l2   # Green cloud bottom
        hourlyRHH = h_low  + l3   # Green cloud top
        hourlyRLA = h_high - l2   # Red cloud top
        hourlyRLL = h_high - l3   # Red cloud bottom
        longT     = h_low  + l4   # Gray — LONG  T2 target
        shortT    = h_high - l4   # Gray — SHORT T2 target

        # ── Stops: just beyond the L3 cloud boundary (ThinkScript spec) ────
        # long_stop  = h_high - L3 - buffer  →  just below the red cloud bottom
        # short_stop = h_low  + L3 + buffer  →  just above the green cloud top
        ts, tv = _tick(api_symbol)
        stop_translated = (dollar_risk / tv) * ts   # small buffer, e.g. 10 pts for /YM
        long_stop  = round((hourlyRLL - stop_translated) / ts) * ts
        short_stop = round((hourlyRHH + stop_translated) / ts) * ts

        # ── T1 (1:1 risk-reward from entry, per ThinkScript label formula) ──
        # long_t1  = 2 * entry - stop   →  mirrors the risk on the upside
        # short_t1 = entry - (stop - entry)
        long_risk  = hourlyRLH - long_stop
        short_risk = short_stop - hourlyRHL
        long_t1    = round((hourlyRLH + long_risk)  / ts) * ts
        short_t1   = round((hourlyRHL - short_risk) / ts) * ts

        # ── HBMR signal state ────────────────────────────────────────────
        # Phase 1 = CR bias active (no time limit — holds until IB breached opposite side)
        # Phase 2 = no bias set — position-driven, both sides shown
        phase1 = bool(daily_bias)

        signal_state: str   # 'NEAR' | 'ENTRY'
        side: str
        entry: float
        stop: float
        t1: float
        target: float

        mid = (shortT + longT) / 2   # midpoint of neutral zone

        if phase1 and daily_bias:
            # ── Phase 1: strict bias ──────────────────────────────────────
            # 30% of L1 minimum approach zone (same as Phase 2)
            _p1_near_buf   = l1 * 0.30
            _p1_near_long  = max(shortT, hourlyRLH + _p1_near_buf)
            _p1_near_short = min(longT,  hourlyRHL - _p1_near_buf)

            if daily_bias == 'LONG':
                side   = 'LONG'
                entry  = hourlyRLH
                stop   = long_stop
                t1     = long_t1
                target = longT
                if last_price > _p1_near_long:
                    signal_state = 'NEUTRAL'   # waiting for retreat
                elif last_price <= hourlyRLH:
                    signal_state = 'ENTRY'
                else:
                    signal_state = 'NEAR'
            else:  # SHORT bias
                side   = 'SHORT'
                entry  = hourlyRHL
                stop   = short_stop
                t1     = short_t1
                target = shortT
                if last_price < _p1_near_short:
                    signal_state = 'NEUTRAL'   # waiting for bounce
                elif last_price >= hourlyRHL:
                    signal_state = 'ENTRY'
                else:
                    signal_state = 'NEAR'
        else:
            # ── Phase 2 / pre-market: position-driven ────────────────────
            # Position % within the full L1-to-L1 range (0% = LONG L1, 100% = SHORT L1)
            l1_range = hourlyRHL - hourlyRLH
            pos_pct  = ((last_price - hourlyRLH) / l1_range * 100) if l1_range > 0 else 50.0

            # Guaranteed minimum NEAR approach zone = 30% of L1 on each side.
            # When l4 is very close to l1 the gray line barely precedes entry;
            # this ensures NEAR activates early enough to be actionable.
            _near_buf   = l1 * 0.30
            _near_long  = max(shortT, hourlyRLH + _near_buf)   # LONG: at most 30% of L1 above entry
            _near_short = min(longT,  hourlyRHL - _near_buf)   # SHORT: at least 30% of L1 below entry

            if last_price <= hourlyRLH:
                # At or beyond LONG L1 — ENTRY LONG
                side         = 'LONG'
                entry, stop, t1, target = hourlyRLH, long_stop, long_t1, longT
                signal_state = 'ENTRY'
            elif last_price <= _near_long:
                # Within 30% of L1 above LONG entry (or gray line, whichever is farther) — NEAR LONG
                side         = 'LONG'
                entry, stop, t1, target = hourlyRLH, long_stop, long_t1, longT
                signal_state = 'NEAR'
            elif pos_pct < 25:
                # Lower quarter of L1 range — neutral but drifting LONG
                side         = 'LONG'
                entry, stop, t1, target = hourlyRLH, long_stop, long_t1, longT
                signal_state = 'NEUTRAL'
            elif pos_pct <= 75:
                # Middle 50% — pure neutral, show nearest side by midpoint
                side         = 'LONG' if last_price <= mid else 'SHORT'
                entry  = hourlyRLH if side == 'LONG' else hourlyRHL
                stop   = long_stop if side == 'LONG' else short_stop
                t1     = long_t1   if side == 'LONG' else short_t1
                target = longT     if side == 'LONG' else shortT
                signal_state = 'NEUTRAL'
            elif last_price < _near_short:
                # Upper quarter of L1 range — neutral but drifting SHORT (not yet in approach zone)
                side         = 'SHORT'
                entry, stop, t1, target = hourlyRHL, short_stop, short_t1, shortT
                signal_state = 'NEUTRAL'
            elif last_price < hourlyRHL:
                # Within 30% of L1 below SHORT entry (or gray line, whichever is farther) — NEAR SHORT
                side         = 'SHORT'
                entry, stop, t1, target = hourlyRHL, short_stop, short_t1, shortT
                signal_state = 'NEAR'
            else:
                # At or beyond SHORT L1 — ENTRY SHORT
                side         = 'SHORT'
                entry, stop, t1, target = hourlyRHL, short_stop, short_t1, shortT
                signal_state = 'ENTRY'

        typical = l3
        swing_pct = round(current_range / typical * 100, 1) if typical else 0

        # ── Guard against stale/inverted or near-zero-profit levels ─────────
        # Two failure modes:
        #   1. Fully inverted: target ≤ entry (range ≥ l1+l4). Geometrically
        #      impossible — signal can never reach a profit.
        #   2. Near-zero profit: target barely above entry. Happens when the
        #      hourly range nearly equals l1+l4 (e.g., PREV 2-hour range close
        #      to the single-hour statistical l1+l4, or unusual intraday move).
        #      Threshold: profit zone < 10% of L1 — at that point the T2 target
        #      sits essentially on top of the entry and the signal is not viable.
        if side == 'LONG' and (
            target <= stop or target <= entry
            or (target - entry) < l1 * 0.10
            or last_price <= stop          # stop already breached — signal dead
        ):
            signal_state = 'NEUTRAL'
        elif side == 'SHORT' and (
            target >= stop or target >= entry
            or (entry - target) < l1 * 0.10
            or last_price >= stop          # stop already breached — signal dead
        ):
            signal_state = 'NEUTRAL'

        results.append({
            'symbol'        : symbol_display,
            'api_symbol'    : api_symbol,
            'side'          : side,
            'model'         : label,
            'signal_state'  : signal_state,       # 'NEAR' | 'ENTRY'
            'daily_bias'    : daily_bias,          # 'LONG' | 'SHORT' | None
            'entry'         : round(entry,  4),
            'stop'          : round(stop,   4),
            't1'            : round(t1,     4),
            'target'        : round(target, 4),   # T2 (gray)
            'lower_gray'    : round(shortT, 4),   # h_high - L4  (LONG trigger)
            'upper_gray'    : round(longT,  4),   # h_low  + L4  (SHORT trigger)
            'last'          : round(last_price, 4),
            'hour_high'     : round(h_high, 4),
            'hour_low'      : round(h_low,  4),
            'swing_pct'     : swing_pct,
            'typical_pct'   : round(100.0, 1),
            'typical_range' : round(typical, 4),
            'current_range' : round(current_range, 4),
            'hour_et'       : h,
            'l1': round(l1, 5), 'l2': round(l2, 5),
            'l3': round(l3, 5), 'l4': round(l4, 5),
        })

    # ── CR (Clearing Range) fade-trade signal ───────────────────────────────────
    # Fade strategy: after IB extreme is breached, wait for price to RETREAT BACK
    # to the CR midpoint — that is the fade entry.
    #
    # State machine:
    #   NEAR  — breach detected; price has NOT yet returned to mid (waiting)
    #   ENTRY — price has retreated back to/through the CR mid (fade entry live)
    #
    # LONG breach (price broke above entry_long): fade by selling the retreat to mid.
    # SHORT breach (price broke below entry_short): fade by buying the bounce to mid.
    #
    # Entry = CR mid, Stop = opposite CR extreme, Target = breach extreme.
    # Sticky logic in main.py keeps NEAR visible for 4 cycles after it first fires.
    if cr and cr.get('complete') and cr_breached:
        cr_mid         = cr['mid']
        cr_entry_long  = cr['entry_long']
        cr_entry_short = cr['entry_short']
        ts, tv = _tick(api_symbol)

        if cr_breached == 'LONG':
            cr_side   = 'LONG'
            entry_cr  = round(cr_mid / ts) * ts
            stop_cr   = cr_entry_short
            target_cr = cr_entry_long
            t1_cr     = round((2 * cr_mid - cr_entry_short) / ts) * ts
            # ENTRY when price retreats back to/below mid; TRENDING while still above (breakout holding)
            cr_state  = 'ENTRY' if last_price <= cr_mid else 'TRENDING'
        else:  # SHORT breach
            cr_side   = 'SHORT'
            entry_cr  = round(cr_mid / ts) * ts
            stop_cr   = cr_entry_long
            target_cr = cr_entry_short
            t1_cr     = round((2 * cr_mid - cr_entry_long) / ts) * ts
            # ENTRY when price bounces back to/above mid; TRENDING while still above (breakdown holding)
            cr_state  = 'ENTRY' if last_price >= cr_mid else 'TRENDING'

        results.append({
            'symbol'        : symbol_display,
            'api_symbol'    : api_symbol,
            'side'          : cr_side,
            'model'         : 'CR',
            'signal_state'  : cr_state,
            'daily_bias'    : daily_bias,
            'entry'         : round(entry_cr,  4),
            'stop'          : round(stop_cr,   4),
            't1'            : round(t1_cr,     4),
            'target'        : round(target_cr, 4),
            'lower_gray'    : round(cr_entry_short, 4),   # CR Low breach level
            'upper_gray'    : round(cr_entry_long,  4),   # CR High breach level
            'last'          : round(last_price, 4),
            'hour_high'     : round(cr['high'], 4),
            'hour_low'      : round(cr['low'],  4),
            'swing_pct'     : 0.0,
            'typical_pct'   : 0.0,
            'typical_range' : round(cr_entry_long - cr_entry_short, 4),
            'current_range' : round(cr_entry_long - cr_entry_short, 4),
            'hour_et'       : h,
            'l1': 0.0, 'l2': 0.0, 'l3': 0.0, 'l4': 0.0,
        })

    return results or None
