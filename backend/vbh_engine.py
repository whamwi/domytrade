def _resolve(table: dict, base: str) -> list[tuple] | None:
    """Follow None aliases to the canonical symbol's data."""
    row = table.get(base)
    if row is not None:
        return row
    # Try aliases
    aliases = {'/MES': '/ES', '/MYM': '/YM', '/MNQ': '/NQ',
               '/MCL': '/CL', '/MGC': '/GC', '/M2K': '/RTY'}
    canonical = aliases.get(base)
    if canonical:
        return table.get(canonical)
    return None


