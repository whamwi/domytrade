def _resolve(table: dict, base: str) -> list[tuple] | None:
    """Follow None aliases to the canonical symbol's data."""
    row = table.get(base)
    if row is not None:
        return row
    # Try aliases
    aliases = {'/MES': '/ES', '/MYM': '/YM', '/MNQ': '/NQ',
               '/MCL': '/CL', '/MGC': '/GC', '/M2K': '/RTY'}
    canonical = aliases.get(base)
    if canonical:
        return table.get(canonical)
    return None


# Confirmed k-ratios from 3-week cross-week analysis of TOS Aggressive study files.
# L2 = mean hourly range (ATR center).  All other levels are fixed fractions of L2.
# k1 + k3 = 2.000 exactly → L1 = mean − σ,  L3 = mean + σ  (CV ≈ 14.7%)
# Using fixed ratios avoids the inversion bug that arises when sample CV is high
# (mean − σ formula collapses L1/L4 when only a few observations are available).
_K_AGG = (0.8527, 1.0000, 1.1473, 0.7960)   # L1, L2, L3, L4  — Aggressive (~1σ)
_K_CON = (0.7054, 1.0000, 1.2946, 0.5920)   # L1, L2, L3, L4  — Conservative (~2σ)


def _build_hourly_ranges(candles: list[dict]) -> dict[int, list[float]]:
    """Merge sub-hourly candles into per-ET-hour buckets and return H-L ranges."""
    buckets: dict[tuple, dict] = {}
    for c in candles:
        dt  = datetime.fromtimestamp(c['datetime'] / 1000, tz=timezone.utc).astimezone(ET)
        key = (dt.date(), dt.hour)
        if key not in buckets:
            buckets[key] = {'high': c['high'], 'low': c['low']}
        else:
            if c['high'] > buckets[key]['high']:
                buckets[key]['high'] = c['high']
            if c['low']  < buckets[key]['low']:
                buckets[key]['low']  = c['low']

    hour_ranges: dict[int, list[float]] = defaultdict(list)
    for (_, h), b in buckets.items():
        r = b['high'] - b['low']
        if r > 0:
            hour_ranges[h].append(r)
    return hour_ranges


def _compute_dynamic(candles: list[dict], k_ratios: tuple = _K_AGG) -> dict[int, tuple]:
    """Compute L1/L2/L3/L4 per ET hour from Schwab sub-hourly candles.

    Used as an automatic fallback for symbols that have no hardcoded constants.

    Method — for each ET hour, take the mean hourly range (= L2, the ATR center),
    then scale by the confirmed VBH k-ratios to obtain L1/L3/L4.  This guarantees
    L4 < L1 < L2 < L3 at every hour regardless of sample size or volatility regime.

    Previous approach (mean ± σ) caused L1/L4 to collapse toward zero when CV was
    high (few observations), producing an inversion where the gray T2 target landed
    below the cyan entry trigger — the so-called "cyan above gray" bug.
    """
    k1, k2, k3, k4 = k_ratios
    hour_ranges = _build_hourly_ranges(candles)

    result: dict[int, tuple] = {}
    for h in range(24):
        rs = hour_ranges.get(h, [])
        if len(rs) < 3:
            result[h] = (0.0, 0.0, 0.0, 0.0)
            continue
        l2 = sum(rs) / len(rs)           # ATR center = mean hourly range
        result[h] = (
            round(l2 * k1, 5),
            round(l2,      5),
            round(l2 * k3, 5),
            round(l2 * k4, 5),
        )

    return result


def compute_stats(candles: list[dict], api_symbol: str = '') -> dict[int, tuple]:
    """Return {hour: (L1,L2,L3,L4)} — hardcoded AGG constants when available,
    otherwise computed dynamically from the provided candles using AGG k-ratios."""
    base = api_symbol.split(':')[0]
    rows = _resolve(_AGG_2022, base)
    if rows is not None:
        return {h: rows[h] for h in range(24)}
    return _compute_dynamic(candles, _K_AGG)


def compute_stats_con(candles: list[dict], api_symbol: str = '') -> dict[int, tuple]:
    """Return {hour: (L1,L2,L3,L4)} — hardcoded CON constants when available,
    otherwise computed dynamically from the provided candles using CON k-ratios
    (~2σ spread: wider cloud, tighter entry zone than AGG)."""
    base = api_symbol.split(':')[0]
    rows = _resolve(_CON_2022, base)
    if rows is not None:
        return {h: rows[h] for h in range(24)}
    return _compute_dynamic(candles, _K_CON)


def make_signal(
    symbol_display: str,
    api_symbol: str,
    current_hour_ohlc: dict,
    last_price: float,
    stats_agg: dict,
    stats_con: dict,
    dollar_risk: float = 50.0,
    hour_override: int | None = None,
    daily_bias: str | None = None,   # 'LONG' | 'SHORT' — RTH open vs prev_settle
    et_minute: int | None = None,    # minutes since midnight ET, for phase detection
) -> list[dict] | None:
    """
    Build signal rows for one symbol using HBMR rules.

    HBMR level ordering (low → high price):
      hourlyRLH (h_high−L1)  ← LONG entry
      shortT    (h_high−L4)  ← NEAR/NEUTRAL boundary (LONG side)
          [NEUTRAL ZONE]
      longT     (h_low+L4)   ← NEAR/NEUTRAL boundary (SHORT side)
      hourlyRHL (h_low+L1)   ← SHORT entry

    Phase 1 (9:30–11:00 ET): bias-driven, strict side suppression.
    Phase 2 (after 11:00 ET): position-driven, neutral zone suppressed.
    """
    now_et = datetime.now(ET)
    h = hour_override if hour_override is not None else now_et.hour

    agg = stats_agg.get(h, (0, 0, 0, 0))
    con = stats_con.get(h, (0, 0, 0, 0))

    if agg[2] == 0 and con[2] == 0:
        return None   # no data for this hour

    h_open  = current_hour_ohlc.get('open',  last_price)
    h_high  = current_hour_ohlc.get('high',  last_price)
    h_low   = current_hour_ohlc.get('low',   last_price)
    current_range = h_high - h_low

    results = []
    for label, (l1, l2, l3, l4) in [('AGG', agg), ('CON', con)]:
        if l3 == 0:
            continue

        # ── Box levels (exact ThinkScript variable names) ──────────────────
        hourlyRHL = h_low  + l1   # Lower cyan  — SHORT entry trigger
        hourlyRLH = h_high - l1   # Upper cyan  — LONG  entry trigger
        hourlyRHA = h_low  + l2   # Green cloud bottom
        hourlyRHH = h_low  + l3   # Green cloud top
        hourlyRLA = h_high - l2   # Red cloud top
        hourlyRLL = h_high - l3   # Red cloud bottom
        longT     = h_low  + l4   # Gray — LONG  T2 target
        shortT    = h_high - l4   # Gray — SHORT T2 target

        # ── Stops: entry ± $50 equivalent ────────────────────────────────
        ts, tv = _tick(api_symbol)
        stop_translated = (dollar_risk / tv) * ts   # e.g. $50/$5 * 1pt = 10 pts for /YM
        long_stop  = round((hourlyRLH - stop_translated) / ts) * ts
        short_stop = round((hourlyRHL + stop_translated) / ts) * ts

        # ── T1 (1:1 risk-reward from entry) ──────────────────────────────
        long_t1  = round((hourlyRLH + stop_translated) / ts) * ts
        short_t1 = round((hourlyRHL - stop_translated) / ts) * ts

        # ── HBMR signal state ────────────────────────────────────────────
        # Phase 1 = 9:30–11:00 (bias-driven, strict suppression of opposing side)
        # Phase 2 = after 11:00 (position-driven, neutral zone hidden)
        phase1 = et_minute is not None and (9 * 60 + 30) <= et_minute < (11 * 60)

        signal_state: str   # 'NEAR' | 'ENTRY'
        side: str
        entry: float
        stop: float
        t1: float
        target: float

        mid = (shortT + longT) / 2   # midpoint of neutral zone

        if phase1 and daily_bias:
            # ── Phase 1: strict bias ──────────────────────────────────────
            if daily_bias == 'LONG':
                side   = 'LONG'
                entry  = hourlyRLH
                stop   = long_stop
                t1     = long_t1
                target = longT
                if last_price > shortT:
                    signal_state = 'NEUTRAL'   # waiting for retreat
                elif last_price <= hourlyRLH:
                    signal_state = 'ENTRY'
                else:
                    signal_state = 'NEAR'
            else:  # SHORT bias
                side   = 'SHORT'
                entry  = hourlyRHL
                stop   = short_stop
                t1     = short_t1
                target = shortT
                if last_price < longT:
                    signal_state = 'NEUTRAL'   # waiting for bounce
                elif last_price >= hourlyRHL:
                    signal_state = 'ENTRY'
                else:
                    signal_state = 'NEAR'
        else:
            # ── Phase 2 / pre-market: position-driven ────────────────────
            # Position % within the full L1-to-L1 range (0% = LONG L1, 100% = SHORT L1)
            l1_range = hourlyRHL - hourlyRLH
            pos_pct  = ((last_price - hourlyRLH) / l1_range * 100) if l1_range > 0 else 50.0

            if last_price <= hourlyRLH:
                # At or beyond LONG L1 — ENTRY LONG
                side         = 'LONG'
                entry, stop, t1, target = hourlyRLH, long_stop, long_t1, longT
                signal_state = 'ENTRY'
            elif last_price <= shortT:
                # Between LONG L1 and lower gray — NEAR LONG
                side         = 'LONG'
                entry, stop, t1, target = hourlyRLH, long_stop, long_t1, longT
                signal_state = 'NEAR'
            elif pos_pct < 25:
                # Lower quarter of L1 range — neutral but drifting LONG
                side         = 'LONG'
                entry, stop, t1, target = hourlyRLH, long_stop, long_t1, longT
                signal_state = 'NEUTRAL'
            elif pos_pct <= 75:
                # Middle 50% — pure neutral, show nearest side by midpoint
                side         = 'LONG' if last_price <= mid else 'SHORT'
                entry  = hourlyRLH if side == 'LONG' else hourlyRHL
                stop   = long_stop if side == 'LONG' else short_stop
                t1     = long_t1   if side == 'LONG' else short_t1
                target = longT     if side == 'LONG' else shortT
                signal_state = 'NEUTRAL'
            elif last_price < longT:
                # Upper quarter of L1 range — neutral but drifting SHORT
                side         = 'SHORT'
                entry, stop, t1, target = hourlyRHL, short_stop, short_t1, shortT
                signal_state = 'NEUTRAL'
            elif last_price < hourlyRHL:
                # Between upper gray and SHORT L1 — NEAR SHORT
                side         = 'SHORT'
                entry, stop, t1, target = hourlyRHL, short_stop, short_t1, shortT
                signal_state = 'NEAR'
            else:
                # At or beyond SHORT L1 — ENTRY SHORT
                side         = 'SHORT'
                entry, stop, t1, target = hourlyRHL, short_stop, short_t1, shortT
                signal_state = 'ENTRY'

        typical = l3
        swing_pct = round(current_range / typical * 100, 1) if typical else 0

        results.append({
            'symbol'        : symbol_display,
            'api_symbol'    : api_symbol,
            'side'          : side,
            'model'         : label,
            'signal_state'  : signal_state,       # 'NEAR' | 'ENTRY'
            'daily_bias'    : daily_bias,          # 'LONG' | 'SHORT' | None
            'entry'         : round(entry,  4),
            'stop'          : round(stop,   4),
            't1'            : round(t1,     4),
            'target'        : round(target, 4),   # T2 (gray)
            'lower_gray'    : round(shortT, 4),   # h_high - L4  (LONG trigger)
            'upper_gray'    : round(longT,  4),   # h_low  + L4  (SHORT trigger)
            'last'          : round(last_price, 4),
            'hour_high'     : round(h_high, 4),
            'hour_low'      : round(h_low,  4),
            'swing_pct'     : swing_pct,
            'typical_pct'   : round(100.0, 1),
            'typical_range' : round(typical, 4),
            'current_range' : round(current_range, 4),
            'hour_et'       : h,
            'l1': round(l1, 5), 'l2': round(l2, 5),
            'l3': round(l3, 5), 'l4': round(l4, 5),
        })

    return results or None
