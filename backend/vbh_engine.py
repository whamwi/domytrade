def make_signal(
    symbol_display: str,
    api_symbol: str,
    current_hour_ohlc: dict,
    last_price: float,
    stats_agg: dict,
    stats_con: dict,
    dollar_risk: float = 50.0,
    hour_override: int | None = None,
) -> list[dict] | None:
    """
    Build signal rows for one symbol, matching the TOS VBH ThinkScript exactly.
    Returns a list (one per model with data) or None.
    """
    now_et = datetime.now(ET)
    h = hour_override if hour_override is not None else now_et.hour

    agg = stats_agg.get(h, (0, 0, 0, 0))
    con = stats_con.get(h, (0, 0, 0, 0))

    if agg[2] == 0 and con[2] == 0:
        return None   # no data for this hour

    h_open  = current_hour_ohlc.get('open',  last_price)
    h_high  = current_hour_ohlc.get('high',  last_price)
    h_low   = current_hour_ohlc.get('low',   last_price)
    current_range = h_high - h_low

    results = []
    for label, (l1, l2, l3, l4) in [('AGG', agg), ('CON', con)]:
        if l3 == 0:
            continue

        # ── Box levels (exact ThinkScript variable names) ──────────────────
        hourlyRHL = h_low  + l1   # Lower cyan  — SHORT entry trigger
        hourlyRLH = h_high - l1   # Upper cyan  — LONG  entry trigger
        hourlyRHA = h_low  + l2   # Green cloud bottom
        hourlyRHH = h_low  + l3   # Green cloud top
        hourlyRLA = h_high - l2   # Red cloud top
        hourlyRLL = h_high - l3   # Red cloud bottom
        longT     = h_low  + l4   # Gray — LONG  T2 target
        shortT    = h_high - l4   # Gray — SHORT T2 target

        # ── Stops: entry ± $50 equivalent ────────────────────────────────
        ts, tv = _tick(api_symbol)
        stop_translated = (dollar_risk / tv) * ts   # e.g. $50/$5 * 1pt = 10 pts for /YM
        long_stop  = round((hourlyRLH - stop_translated) / ts) * ts
        short_stop = round((hourlyRHL + stop_translated) / ts) * ts

        # ── T1 (1:1 risk-reward from entry) ──────────────────────────────
        long_t1  = round((hourlyRLH + stop_translated) / ts) * ts
        short_t1 = round((hourlyRHL - stop_translated) / ts) * ts

        # ── Signal side ───────────────────────────────────────────────────
        # Gray lines are the TRIGGERS; cyan lines are the ENTRIES.
        # Lower gray = shortT = h_high - L4  → LONG trigger
        # Upper gray = longT  = h_low  + L4  → SHORT trigger
        # No mid-box signal — only fire when near a gray level.
        if last_price <= hourlyRLH:
            side   = 'LONG'
            entry  = hourlyRLH   # lower cyan
            stop   = long_stop
            t1     = long_t1
            target = longT       # upper gray (T2)
        elif last_price >= hourlyRHL:
            side   = 'SHORT'
            entry  = hourlyRHL   # upper cyan
            stop   = short_stop
            t1     = short_t1
            target = shortT      # lower gray (T2)
        else:
            # Price inside box — show nearest cyan (directional bias only)
            mid = (hourlyRLH + hourlyRHL) / 2
            if last_price <= mid:
                side   = 'LONG'
                entry  = hourlyRLH
                stop   = long_stop
                t1     = long_t1
                target = longT
            else:
                side   = 'SHORT'
                entry  = hourlyRHL
                stop   = short_stop
                t1     = short_t1
                target = shortT

        # ── Alert: price within 5 ticks of the trigger gray ──────────────
        trigger_gray = shortT if side == 'LONG' else longT   # relevant gray for this side
        near_gray = abs(last_price - trigger_gray) <= 5 * ts

        typical = l3
        swing_pct = round(current_range / typical * 100, 1) if typical else 0

        results.append({
            'symbol'        : symbol_display,
            'api_symbol'    : api_symbol,
            'side'          : side,
            'model'         : label,
            'entry'         : round(entry,  4),
            'stop'          : round(stop,   4),
            't1'            : round(t1,     4),
            'target'        : round(target, 4),   # T2 (gray)
            'lower_gray'    : round(shortT, 4),   # h_high - L4  (LONG trigger)
            'upper_gray'    : round(longT,  4),   # h_low  + L4  (SHORT trigger)
            'near_gray'     : near_gray,           # price within 5 ticks of trigger gray
            'last'          : round(last_price, 4),
            'hour_high'     : round(h_high, 4),
            'hour_low'      : round(h_low,  4),
            'swing_pct'     : swing_pct,
            'typical_pct'   : round(100.0, 1),
            'typical_range' : round(typical, 4),
            'current_range' : round(current_range, 4),
            'hour_et'       : h,
            'l1': round(l1, 5), 'l2': round(l2, 5),
            'l3': round(l3, 5), 'l4': round(l4, 5),
        })

    return results or None
