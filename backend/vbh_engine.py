def _build_hourly_ranges(candles: list[dict]) -> dict[int, list[float]]:
    """Merge sub-hourly candles into per-ET-hour buckets and return H-L ranges."""
    buckets: dict[tuple, dict] = {}
    for c in candles:
        dt  = datetime.fromtimestamp(c['datetime'] / 1000, tz=timezone.utc).astimezone(ET)
        key = (dt.date(), dt.hour)
        if key not in buckets:
            buckets[key] = {'high': c['high'], 'low': c['low']}
        else:
            if c['high'] > buckets[key]['high']:
                buckets[key]['high'] = c['high']
            if c['low']  < buckets[key]['low']:
                buckets[key]['low']  = c['low']

    hour_ranges: dict[int, list[float]] = defaultdict(list)
    for (_, h), b in buckets.items():
        r = b['high'] - b['low']
        if r > 0:
            hour_ranges[h].append(r)
    return hour_ranges


def _compute_dynamic(candles: list[dict], shift: float = 0.0) -> dict[int, tuple]:
    """Compute L1/L2/L3/L4 per ET hour from Schwab sub-hourly candles.

    Replicates the 2022 original methodology with σ_eff cap:
      μ        = mean hourly range,  σ = sample std dev
      σ_eff    = min(σ, μ × 0.1473)  ← cap matches 2022 study character:
                  _AGG_2022 shows a uniform σ/μ = 14.730% (±0.003%) across all 240
                  non-zero RTH pairs, confirming the study used a fixed k-ratio rather
                  than raw σ.  Capping at 14.73% keeps L1 ≥ 0.8527μ, consistent
                  with 2022 inversion frequency.

    shift controls how far the ±σ band is displaced from the mean:
      AGG  (shift=0.0): L1=μ-σ_eff,         L2=μ,           L3=μ+σ_eff
      CON  (shift=2.4): L1=μ+1.4σ_eff,      L2=μ+2.4σ_eff,  L3=μ+3.4σ_eff
      WIDE (shift=4.0): L1=μ+3.0σ_eff,      L2=μ+4.0σ_eff,  L3=μ+5.0σ_eff

    L4 is 0.385·σ_eff LESS than L1 (T2 target sits outside the entry line).
    (0.385 derived from _AGG_2022: (L1−L4)/σ = 0.3849 ±0.003, all 240 pairs)
    """
    hour_ranges = _build_hourly_ranges(candles)

    result: dict[int, tuple] = {}
    for h in range(24):
        rs = hour_ranges.get(h, [])
        if len(rs) < 3:
            result[h] = (0.0, 0.0, 0.0, 0.0)
            continue
        mu  = sum(rs) / len(rs)
        var = sum((x - mu) ** 2 for x in rs) / (len(rs) - 1)
        sigma = var ** 0.5

        # Cap σ at 14.73% of μ — matches 2022 study fixed k-ratio character
        sigma_eff = min(sigma, mu * 0.1473)

        l2 = mu + shift * sigma_eff
        l1 = max(l2 - sigma_eff, 0.0)
        l3 = l2 + sigma_eff
        l4 = max(l1 - sigma_eff * 0.385, 0.0)  # T2: 0.385·σ_eff outside entry line

        result[h] = (round(l1,5), round(l2,5), round(l3,5), round(l4,5))

    return result


def compute_stats(candles: list[dict], api_symbol: str = '') -> dict[int, tuple]:
    """Return {hour: (L1,L2,L3,L4)} for the Aggressive model.

    Priority order:
      1. DB cache (_stats_db) — live-updated via update_vbh_tables.py + Supabase.
         Used when the cache is populated and has at least one non-zero RTH hour.
      2. Hardcoded 2022 tables (_AGG_2022) — original TOS study constants.
         Used for all symbols present in the hardcoded dict.
      3. Dynamic computation from candles — last resort for unknown symbols.
    """
    base = api_symbol.split(':')[0]

    # ── 1. DB cache (preferred — live data) ───────────────────────────────────
    # Resolve mini-contract aliases so /MES → /ES lookup works in _stats_db too.
    aliases = {'/MES': '/ES', '/MYM': '/YM', '/MNQ': '/NQ',
               '/MCL': '/CL', '/MGC': '/GC', '/M2K': '/RTY'}
    db_key = aliases.get(base, base)
    if db_key in _stats_db and _stats_db[db_key].get('AGG'):
        db_rows = _stats_db[db_key]['AGG']
        # Only use DB rows if at least one non-zero hour exists (guards cold DB)
        if any(r[1] > 0 for r in db_rows):
            return {h: db_rows[h] for h in range(24)}

    # ── 2. Hardcoded 2022 constants (reliable fallback) ───────────────────────
    rows = _resolve(_AGG_2022, base)
    if rows is not None:
        return {h: rows[h] for h in range(24)}

    # ── 3. Dynamic computation from caller-supplied candles ───────────────────
    return _compute_dynamic(candles, shift=0.0)


